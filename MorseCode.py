changedicts = {
    "・":"トン"
    ,"－":"ツー"
}

def encodeMorseCode(charas):
    mcode = ""
    for chara in charas:
        try:
            mcode += " " + dicts[chara]
        except:
            mcode += " "
    return mcode

def encodeVoiceCode(mcodes):
    voicecode = ""
    for mcode in mcodes:
        try:
            voicecode += " " + changedicts[mcode]
        except:
            voicecode += " "
    return voicecode

